# SISTEMA DECIMAL
a = 10
print(f"a decimal: {a}")

# SISTEMA BINARIO
a = 0b1010
print(f"a binario: {a}")

# SISTEMA OCTAL
a = 0o12
print(f"a octal: {a}")

# SISTEMA HEXADECIMAL
a = 0xA
print(f"a hexadecimal: {a}")


# BASE DECIMAL
a = int('23', 10)
print(f"a = base decimal: {a}")

# BASE BINARIO
a = int('10111', 2)
print(f"a = base binario: {a}")

# BASE OCTAL
a = int('27', 8)
print(f"a = base octal: {a}")

# BASE HEXADECIMAL
a = int('17', 16)
print(f"a = base hexadecimal: {a}")

# Base 5 sus valores son de 0 a 4
a = int('344', 5)
print(f"a = Base 5: {a}")
